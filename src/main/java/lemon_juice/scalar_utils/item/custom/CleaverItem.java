import java.util.List;

public class CleaverItem extends SwordItem {
    public CleaverItem(Tier tier, int attackDamage, float speed, Properties properties) {
        super(tier, attackDamage + 2, speed - 1.0F, properties);
    }

    @Override
    public void appendHoverText(ItemStack itemStack, @Nullable Level level, List<Component> components, TooltipFlag tooltipFlag) {
        components.add(Component.translatable("cleaver.tooltip"));
    }
}
